public class Pattern3 {
    public static void main(String[] args) {
        for(int i=1;i<=4;i++){
            for(int j=1;j==1;j++){
                System.out.print("*");
            }
            System.out.println();
        }
        for(int k=1;k<=5;k++){
            System.out.print("* ");
        }
    }
    
}
